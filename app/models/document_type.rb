class DocumentType < ActiveRecord::Base
#--------------------------------------------------------------------------------------------------------------------------------

  include CommonStuff

#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------

  #--- validations
  validates_presence_of :name
  
  #--- associations
  has_many :documents

#--------------------------------------------------------------------------------------------------------------------------------

  def get_file_path
    return "#{self.file_path}/" unless self.file_path.blank?
    return ''
  end
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------
end
