class BrandsController < ApplicationController
#--------------------------------------------------------------------------------------------------------------------------------

  include RController
  
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------

  def filter_settings
    @filter.register_search_fields('id','code','name','title','description','trace')
    @filter.set_sort_by_and_order('trace','ASC')
  end
  
#--------------------------------------------------------------------------------------------------------------------------------

  def do_after_save
    @object.reset_trace(:save=>true,:recursive=>true)
  end
  def do_before_each
    @class_flags = live_data_flag
  end
  
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------
end
