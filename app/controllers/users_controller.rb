class UsersController < ApplicationController
#--------------------------------------------------------------------------------------------------------------------------------

  include RController
  include RControllerExtras
  
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------

  def filter_settings
    @filter.register_search_fields('id','email')
    @filter.set_sort_by_and_order('name','ASC')
  end

  def do_before_each
    @class_flags = live_data_flag
  end
  
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------
#--------------------------------------------------------------------------------------------------------------------------------
end
